//Write an expression that calculates rectangle’s area by given width and height.

using System;
class RectangleArea
{
    static void Main()
    {
        // деклариране на променливите за дължина и височина на правоъгълника
        int width = 5;
        int height = 7;

        // Директно пресмятане на лицето на фигурата при извеждането на резултата на екрана!
        Console.WriteLine("The area of a rectangle with width = {0} and height = {1} is {2}!", width, height, width * height);
    }
}

